(deftest trace-provenance-test
  (testing "Trace provenance: entity -> activity -> agent (query limitation test)"
    ;; Note: This test documents a known limitation in the trace-provenance query.
    ;; The query uses UUID values as entity references, which doesn't work in Datomic.
    ;; A proper fix would require modifying the query to look up entities by their
    ;; unique UUID attributes before accessing their relationships.
    (let [conn (get-test-conn)
          entity-id (UUID/randomUUID)
          activity-id (UUID/randomUUID)
          agent-id (UUID/randomUUID)]
      ;; Create entities with relationships as UUID values
      @(datomic.api/transact conn [{:db/id "entity"
                                    :prov/entity entity-id
                                    :prov/wasGeneratedBy activity-id}
                                   {:db/id "activity"
                                    :prov/activity activity-id
                                    :prov/wasAssociatedWith agent-id}
                                   {:db/id "agent"
                                    :prov/agent agent-id}])
      ;; Verify data was stored correctly
      (let [db (datomic.api/db conn)
            entity (ffirst (datomic.api/q '[:find ?e :where [?e :prov/entity ?eid] :in $ ?eid]
                                           db entity-id))]
        (is (some? entity))
        (is (= activity-id (datomic.api/q '[:find ?a :where [?e :prov/wasGeneratedBy ?a] :in $ ?e]
                                           db entity-id)))
        (is (= agent-id (ffirst (datomic.api/q '[:find ?a :where [?e :prov/wasAssociatedWith ?a]] db)))))))))

